console.log('Strict Equality checks:')
console.log('1 === 1: ', 1 === 1) // Output: true
console.log("'hello' === 'hello': ", 'hello' === 'hello') // Output: true
console.log('true === true: ', true === true) // Output: true
console.log('false === false: ', false === false) // Output: true
console.log('null === null: ', null === null) // Output: true
console.log('undefined === undefined: ', undefined === undefined) // Output: true
console.log("1 === '1': ", 1 === '1') // Output: false
console.log("'hello' === 'Hello': ", 'hello' === 'Hello') // Output: false
console.log('')

// ======================================================

console.log('Loose Equality checks:')
console.log('1 == 1: ', 1 == 1) // Output: true
console.log("'hello' == 'hello': ", 'hello' == 'hello') // Output: true
console.log('true == true: ', true == true) // Output: true
console.log('false == false: ', false == false) // Output: true
console.log('null == null: ', null == null) // Output: true
console.log('undefined == undefined: ', undefined == undefined) // Output: true
console.log("1 == '1': ", 1 == '1') // Output: true
console.log("'hello' == 'Hello': ", 'hello' == 'Hello') // Output: false
console.log('')

